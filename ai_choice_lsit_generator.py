@app.route("/generate", methods=["POST"])
def generate():
    data = request.json
    cutoff = data.get("cutoff", "")
    category = data.get("category", "")
    region = data.get("region", "Any")
    count = int(data.get("count", 10))

    if not cutoff or not category:
        return jsonify({"error": "cutoff and category are required"}), 400

    prompt = PROMPT_TEMPLATE.format(cutoff=cutoff, category=category, region=region, count=count)

    try:
        response = model.generate_content(prompt)
        text = response.text.strip()
        # Clean markdown fences if present
        text = re.sub(r"^```(?:json)?\s*", "", text)
        text = re.sub(r"\s*```$", "", text)
        colleges = json.loads(text)
    except Exception as e:
        return jsonify({"error": f"Gemini API error: {str(e)}"}), 500

    student_info = {"cutoff": cutoff, "category": category, "region": region}
    pdf_buf = generate_pdf(student_info, colleges)

    return send_file(pdf_buf, mimetype="application/pdf", as_attachment=True, download_name="tnea_choice_list.pdf")


