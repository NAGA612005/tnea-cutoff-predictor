def wrap_cell(text, style):
    """Wrap text in Paragraph so it breaks to next line when too long."""
    return Paragraph(str(text).replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;"), style)

def generate_pdf(student_info, colleges):
    buf = io.BytesIO()
    doc = SimpleDocTemplate(buf, pagesize=landscape(A4), topMargin=30, bottomMargin=30, leftMargin=25, rightMargin=25)
    styles = getSampleStyleSheet()
    title_style = ParagraphStyle("Title2", parent=styles["Title"], fontSize=18, textColor=colors.HexColor("#1a237e"), alignment=1)
    sub_style = ParagraphStyle("Sub", parent=styles["Normal"], fontSize=11, textColor=colors.HexColor("#333333"), spaceAfter=4, alignment=1)
    cell_style = ParagraphStyle("Cell", fontName="Helvetica", fontSize=8, leading=11, alignment=0, splitLongWords=1, wordWrap='CJK')
    cell_center = ParagraphStyle("CellC", fontName="Helvetica", fontSize=8, leading=11, alignment=1, splitLongWords=1, wordWrap='CJK')
    header_style = ParagraphStyle("Header", fontName="Helvetica-Bold", fontSize=9, leading=12, textColor=colors.white, alignment=1)
    elements = []

    elements.append(Paragraph("TNEA College Choice List", title_style))
    elements.append(Spacer(1, 10))
    elements.append(Paragraph(f"Cutoff: {student_info['cutoff']} &nbsp;|&nbsp; Category: {student_info['category']} &nbsp;|&nbsp; Region: {student_info['region']}", sub_style))
    elements.append(Spacer(1, 14))

    header = [wrap_cell(h, header_style) for h in ["#", "College Name", "Department", "Cutoff Range", "Type", "Location"]]
    data = [header]
    for i, c in enumerate(colleges, 1):
        data.append([
            wrap_cell(i, cell_center),
            wrap_cell(c.get("college", ""), cell_style),
            wrap_cell(c.get("department", ""), cell_style),
            wrap_cell(c.get("cutoff_range", ""), cell_center),
            wrap_cell(c.get("college_type", ""), cell_center),
            wrap_cell(c.get("location", ""), cell_style),
        ])

    pw = landscape(A4)[0] - 50
    col_widths = [pw*0.04, pw*0.34, pw*0.20, pw*0.14, pw*0.14, pw*0.14]
    table = Table(data, colWidths=col_widths, repeatRows=1, splitInRow=1)
    table.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (-1, 0), colors.HexColor("#1a237e")),
        ("VALIGN", (0, 0), (-1, -1), "TOP"),
        ("GRID", (0, 0), (-1, -1), 0.5, colors.HexColor("#cccccc")),
        ("ROWBACKGROUNDS", (0, 1), (-1, -1), [colors.HexColor("#f5f5f5"), colors.white]),
        ("LEFTPADDING", (0, 0), (-1, -1), 6),
        ("RIGHTPADDING", (0, 0), (-1, -1), 6),
        ("TOPPADDING", (0, 0), (-1, -1), 6),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 6),
    ]))
    elements.append(table)
    elements.append(Spacer(1, 20))
    elements.append(Paragraph("Generated by TNEA Choice List Maker | AI-powered suggestions — verify with official TNEA data.",
                              ParagraphStyle("Foot", fontName="Helvetica", fontSize=8, textColor=colors.grey, alignment=1)))
    doc.build(elements)
    buf.seek(0)
    return buf



